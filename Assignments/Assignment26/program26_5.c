#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   : Pattern
//  Description     : It prints numbers starting from range no/2 to no*2
//  Input           : Int
//  Output          : Void
//  Author          : Atharva Vinod Gawade
//  Date            : 20/11/2025
//
///////////////////////////////////////////////////////////////////////////////////////

void Pattern(int iNo)
{
    int iCnt = 0;

    for(iCnt = iNo / 4; iCnt <= iNo * 2; iCnt = iCnt + 2)
    {
        printf("%d\t", iCnt);
    }
} // End of Pattern

//  Time Complexity : O(N)

///////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
///////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter the number of elements\n");
    scanf("%d", &iValue);

    Pattern(iValue);

    return 0;
}

