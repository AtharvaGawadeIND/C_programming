#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   : Pattern
//  Description     : It prints alphabets starting from 'A' up to the given number of elements
//  Input           : Int
//  Output          : Void
//  Author          : Atharva Vinod Gawade
//  Date            : 20/11/2025
//
///////////////////////////////////////////////////////////////////////////////////////

void Pattern(int ino)
{
    int iCnt = 0;
    char ch = 'A';

    for(iCnt = 0; iCnt < ino; iCnt++)
    {
        printf("%c\t", ch);
        ch++;
    }
} // End of Pattern

//  Time Complexity : O(N)

///////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
///////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter the number of elements\n");
    scanf("%d", &iValue);

    Pattern(iValue);

    return 0;
}

